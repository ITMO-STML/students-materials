"""
Все промпты для конвейеров CN (counterfactual negatives) и GP (gradient positives).

Принципы:
1. На русском, потому что работаем с русским корпусом и Qwen хорошо понимает русский.
2. Формат вывода строгий (JSON или одна строка), чтобы парсить без эвристик.
3. Промпты тестировались отдельно — менять с осторожностью, желательно через A/B.
"""

# ──────────────────────────────────────────────────────────────────────
# CN-1: Извлечение фактов
# ──────────────────────────────────────────────────────────────────────
# Принципиальное отличие от слайда: НЕ жёстко зашитая таксономия
# (субъект/действие/результат/число), а open-ended описание типа.
# Это критично для доменной обобщаемости.

FACT_EXTRACTION_SYSTEM = """Ты — эксперт по семантическому анализу. \
Твоя задача — выделять АТОМАРНЫЕ факты в документе, изменение которых \
сделает документ неверным ответом на конкретный запрос.

АТОМАРНЫЙ факт — это ОДНА сущность, ОДНО число, ОДНА дата или ОДНО свойство. \
НЕ предложение, НЕ фраза с несколькими фактами. Разбивай длинные фрагменты \
на отдельные факты."""

FACT_EXTRACTION_USER = """Запрос пользователя:
{query}

Документ, отвечающий на запрос:
{d_plus}

Извлеки АТОМАРНЫЕ факты, изменение каждого из которых превратит документ в НЕВЕРНЫЙ \
ответ на запрос. Игнорируй второстепенные детали, которые на ответ не влияют.

ВАЖНО: каждый факт = ОДНА минимальная единица. Примеры правильного разбиения:

Запрос: "Когда основали компанию X?"
Документ: "Компания X была основана 15 марта 1990 года Иваном Петровым в Москве."
Правильно:
[
  {{"text": "15 марта 1990 года", "type": "дата", "criticality": 5}},
  {{"text": "Иваном Петровым", "type": "имя основателя", "criticality": 4}},
  {{"text": "в Москве", "type": "географическое место", "criticality": 3}}
]
Неправильно:
[
  {{"text": "Компания X была основана 15 марта 1990 года Иваном Петровым в Москве", "type": "факт основания", "criticality": 5}}
]

Для каждого факта укажи:
- "text": точная КОРОТКАЯ подстрока из документа (1-5 слов, verbatim)
- "type": краткое описание типа
- "criticality": 1-5, насколько искажение этого факта сломает ответ на запрос

Стремись найти ВСЕ значимые факты (обычно 3-6 на документ). \
Обязательно включай числа, даты, имена собственные, географические названия.

Верни строго JSON-массив без преамбулы:
[
  {{"text": "...", "type": "...", "criticality": N}},
  ...
]

Если значимых фактов нет — верни [].
"""


# ──────────────────────────────────────────────────────────────────────
# CN-2: Контрфактическая мутация
# ──────────────────────────────────────────────────────────────────────
# Изменяем ТОЛЬКО указанный факт, сохраняя стиль и структуру.

COUNTERFACTUAL_SYSTEM = """Ты — редактор, который создаёт контрфактические \
версии документов: изменяешь точечный факт так, чтобы документ перестал быть \
верным ответом на запрос, но визуально оставался похожим на оригинал."""

COUNTERFACTUAL_USER = """Запрос:
{query}

Оригинальный документ:
{d_plus}

Факт, который нужно изменить: "{fact_text}"
Тип факта: {fact_type}

Создай изменённую версию документа со следующими ограничениями:
1. Сохрани стиль, тон, длину и структуру оригинала.
2. Измени ТОЛЬКО указанный факт и минимально необходимое окружение для \
грамматической связности.
3. Замена должна быть правдоподобной (выглядеть как настоящий текст той же \
тематики), но фактически НЕВЕРНОЙ относительно запроса.
4. НЕ добавляй маркеры неверности ("на самом деле нет", "ошибочно считается" и т.п.).
5. НЕ меняй формат заголовков, абзацев, списков, если они есть.

Верни ТОЛЬКО изменённый документ. Без преамбулы, без объяснений, без кавычек \
вокруг всего текста."""


# ──────────────────────────────────────────────────────────────────────
# GP: Генерация позитивных вариантов с разным уровнем близости
# ──────────────────────────────────────────────────────────────────────
# Древовидная (а не цепочная) генерация: все варианты — из d⁺, а не друг из друга.
# Дополнительно генерируем сразу несколько кандидатов с разной степенью изменения,
# чтобы потом фильтровать по фактическому уровню sim.

GRADIENT_POSITIVE_SYSTEM = """Ты — редактор, который переписывает документ \
разными способами, сохраняя его как корректный ответ на исходный запрос."""

GRADIENT_POSITIVE_USER = """Запрос:
{query}

Исходный документ (он корректно отвечает на запрос):
{d_plus}

Сгенерируй {n_versions} разных перефразированных версий документа. Каждая версия:
- Должна оставаться КОРРЕКТНЫМ и ДОСТАТОЧНЫМ ответом на запрос.
- Должна сохранить все ключевые факты, нужные для ответа.
- Должна заметно отличаться от других версий и от оригинала по лексике, \
структуре и порядку изложения.
- Не должна добавлять выдуманных фактов.

Цельтесь в РАЗНЫЕ уровни близости к оригиналу:
- "near": лексика близка к оригиналу, но порядок предложений и формулировки изменены
- "mid": заметная переформулировка, синонимы, перестроенная структура
- "far": сильное переписывание (другие грамматические конструкции, другой тон), \
но с сохранением ключевых фактов

Верни строго JSON-массив без преамбулы:
[
  {{"level": "near", "text": "..."}},
  {{"level": "mid",  "text": "..."}},
  {{"level": "far",  "text": "..."}},
  ...
]
"""


# ──────────────────────────────────────────────────────────────────────
# Верификация: LLM-judge
# ──────────────────────────────────────────────────────────────────────
# Используется и для CN (является ли d⁻ ответом? Должно быть Нет),
# и для GP (является ли dᵢ ответом? Должно быть Да).
#
# Два режима:
#   - FAST: одна строка Да/Нет. Дёшево, но в Фазе 0 показал 48-61% accuracy
#     на контрфактах — не справляется с числовыми/датовыми изменениями.
#   - COT:  пошаговое рассуждение с явным маркером ВЕРДИКТ в конце.
#           Дороже на 200-300 токенов, но точнее на числах/датах/именах.
# По умолчанию используем COT; FAST оставлен для A/B и для скейла, если COT
# окажется слишком медленным.

# --- FAST вариант (старый) ---
LLM_JUDGE_SYSTEM = """Ты — строгий судья ФАКТИЧЕСКОЙ точности. \
Ты проверяешь, содержит ли документ ВЕРНЫЙ ответ на запрос. \
Обращай особое внимание на числа, даты, имена и названия — \
даже небольшая ошибка в них делает ответ НЕВЕРНЫМ."""

LLM_JUDGE_USER = """Запрос:
{query}

Документ:
{d}

Содержит ли документ ФАКТИЧЕСКИ ВЕРНЫЙ ответ на запрос?

Примеры:
- Запрос: "В каком году произошло событие X?" Документ: "...в 1380 году..." → если на самом деле в 1380, то Да. Если в документе 1370 вместо 1380 — Нет.
- Запрос: "Сколько спутников у планеты Y?" Документ: "...79 спутников..." → если правильный ответ 95, то Нет, даже если документ в остальном правильный.
- Запрос: "Кто написал X?" Документ: "...написал Пушкин..." → если на самом деле Толстой, то Нет.

ВАЖНО: даже ОДНО неверное число, дата, имя или название делает ответ НЕВЕРНЫМ. \
Не оценивай "примерную правильность" — оценивай ТОЧНУЮ.

Ответь СТРОГО одним словом: Да или Нет."""


# --- COT вариант (новый) ---
# Принцип: модель сначала явно выписывает ключевые сущности из документа,
# затем сопоставляет их с тем, что она знает по теме, и только потом выносит вердикт.
# Особое внимание — числам/датам/именам, потому что Фаза 0 показала, что именно
# их judge склонен пропускать при прямом ответе.

LLM_JUDGE_COT_SYSTEM = """Ты — строгий судья ФАКТИЧЕСКОЙ точности. \
Ты рассуждаешь по шагам и в конце выдаёшь однозначный вердикт. \
Особенно внимателен к числам, датам, именам и географическим названиям."""

LLM_JUDGE_COT_USER = """Запрос:
{query}

Документ:
{d}

Определи, содержит ли документ ФАКТИЧЕСКИ ВЕРНЫЙ ответ на запрос. \
Рассуждай строго по шагам, каждый шаг — одна короткая строка.

ШАГ 1 (что спрашивается). Сформулируй, какой именно факт ищет запрос (имя, дата, число, место и т.п.).

ШАГ 2 (что в документе). Выпиши verbatim ключевые сущности из документа, отвечающие на запрос (числа, даты, имена, названия).

ШАГ 3 (проверка). Сопоставь данные документа с тем, что ты знаешь по теме. Правила:
- любое расхождение в числе/дате (хоть на единицу) → ответ НЕВЕРЕН;
- замена имени, фамилии или названия на другое → ответ НЕВЕРЕН;
- замена географического места на другое → ответ НЕВЕРЕН;
- если в документе требуемого факта нет вовсе → ответ НЕВЕРЕН;
- если ты не уверен в правильном ответе, но документ внутренне непротиворечив и правдоподобен → считай ВЕРНЫМ.

ШАГ 4 (вердикт). В ПОСЛЕДНЕЙ строке напиши строго:
ВЕРДИКТ: Да
или
ВЕРДИКТ: Нет"""


# ──────────────────────────────────────────────────────────────────────
# Утилиты для сборки сообщений в формате Qwen chat template
# ──────────────────────────────────────────────────────────────────────

def build_fact_extraction_messages(query: str, d_plus: str) -> list[dict]:
    return [
        {"role": "system", "content": FACT_EXTRACTION_SYSTEM},
        {"role": "user", "content": FACT_EXTRACTION_USER.format(
            query=query, d_plus=d_plus
        )},
    ]


def build_counterfactual_messages(
    query: str, d_plus: str, fact_text: str, fact_type: str
) -> list[dict]:
    return [
        {"role": "system", "content": COUNTERFACTUAL_SYSTEM},
        {"role": "user", "content": COUNTERFACTUAL_USER.format(
            query=query, d_plus=d_plus,
            fact_text=fact_text, fact_type=fact_type,
        )},
    ]


def build_gradient_positive_messages(
    query: str, d_plus: str, n_versions: int = 3
) -> list[dict]:
    return [
        {"role": "system", "content": GRADIENT_POSITIVE_SYSTEM},
        {"role": "user", "content": GRADIENT_POSITIVE_USER.format(
            query=query, d_plus=d_plus, n_versions=n_versions,
        )},
    ]


def build_judge_messages(query: str, d: str) -> list[dict]:
    """FAST режим: одно слово Да/Нет."""
    return [
        {"role": "system", "content": LLM_JUDGE_SYSTEM},
        {"role": "user", "content": LLM_JUDGE_USER.format(query=query, d=d)},
    ]


def build_judge_cot_messages(query: str, d: str) -> list[dict]:
    """COT режим: пошаговое рассуждение + явный вердикт в конце."""
    return [
        {"role": "system", "content": LLM_JUDGE_COT_SYSTEM},
        {"role": "user", "content": LLM_JUDGE_COT_USER.format(query=query, d=d)},
    ]
